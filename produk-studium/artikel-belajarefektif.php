<h2 align=center style='text-align:center'><b><span style='font-size:26.0pt;
line-height:107%;color:windowtext'>CARA BELAJAR YANG EFEKTIF DAN EFISIEN</span></b></h2>

<p class=MsoNormal align=center style='margin-bottom:15.0pt;text-align:center;
line-height:19.5pt'><span style='font-size:12.0pt;font-family:"inherit",serif;
color:black'><img width=505 height=341 id="Picture 1"
src="artikel-belajarefektif_files/image001.jpg"></span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Belajar pagi para
siswa hingga mahasiswa adalah sebuah kewajiban sebagaimana predikatnya sebagai
pelajar. Pelajar yang baik tentunya harus belajar yang baik, belajar yang baik
adalah belajar yang menghasilkan peningkatan pengetahuan, sikap dan atau
keterampilannya. Namun apakah belajar Anda sebagai pelajar sudah efektif dan
efisien? Jika belum, mari simak 7 cara belajar yang efektif dan efisien
berikut:</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><b><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>1.&nbsp; Buat
Suasana Belajar yang Nyaman</span></b></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Yang perlu Anda
lakukan pertama adalah bagaimana cara membangun suasana belajar yang nyaman.
Ada banyak cara untuk membuat mood belajar itu muncul, diantara: Anda bisa
belajar sambil mendengarkan musik, belajar di tempat-tempat yang nyaman
misalnya di taman atau dipinggir danau, atau diruangan ber-AC</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><b><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>&nbsp;2.&nbsp;
Merangkum Pokok Pembelajaran</span></b></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Yah, mencatat
pokok-pokok pembelajaran. Kenapa? Karena jika Anda membaca 1 buku maka akan
butuh waktu yang sangat lama untuk menyelesaikan. Ambilah intisari dari pelajar
tersebut. Hal ini akan membuat kemudahan mengingat dan juga mudah untuk
memahami inti dari pembelajaran tersebut</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>&nbsp;<b>3.&nbsp;
Belajar Bersama</b></span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Metode ini
seringkali di katakan metode yg paling efektif karena dalam suasana belajar
berkelompok yang cukup santai otak menjadi lebih rileks menerima pelajaran/materi
yang akan di serap. Selain itu hal-hal yang belum di ketahui akan lebih mudah
di selesaikan dengan bekerja sama. Maka sangat dianjurkan untuk belajar bersama
untuk menghadapi ujian.</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><b><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>&nbsp;4.&nbsp;
Metode mempersingkat atau memodifikasi menyerupai nama sesuatu</span></b></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Metode ini
digunakan bagi beberapa orang yang kesulitan dalam menghafal dengan cara
menggunakan nama-nama yang hampir mirip untuk mengingat materi. Ini sangat
efektif digunakan dan otak sangat mudah mengingatnya.</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><b><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>&nbsp;5.&nbsp;
Belajar dengan Praktik</span></b></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Belajar sambil
praktek adalah hal yang sangat efektif. Yah, Cara belajar ini juga akan membuat
Anda tak merasa bosan. Misalnya pelajaran IPA seperti Botani atau Avertebrata,
kita bisa belajar sambil mengamati tumbuh-tumbuhan, hewan atau apapun, dengan
itu kita bisa membuat sebuah acara belajar menjadi lebih asyik.</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><b><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>&nbsp;6.&nbsp;
Belajar rutin tapi jangan lama</span></b></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Dengan rutin
belajar Anda akan semakin mudah untuk mengingat hal yang sudah Anda pelajari.
yang perlu Anda lakukan adalah &quot;belajar rutin&quot; bukan &quot;Terlalu
lama belajar&quot;. Seperti belajar saat pagi 45 menit, siang 25 menit, sore 50
menit, malam 1 jam. Cara ini sangat efetif dan pikiran juga akan tetap dalam
keadaan rileks dari pada harus belajar terlalu lama.</span></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><b><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>&nbsp;7.&nbsp;
Mengerti Bukan Menghafal</span></b></p>

<p class=MsoNormal style='margin-bottom:15.0pt;line-height:normal'><span
style='font-size:12.0pt;font-family:"Times New Roman",serif'>Yah, hal yg paling
sering dilakukan oleh siswa atapun mahasiswa ketika ingin menghadapi ujian
adalah menghafal. Sebenarnya tidak salah cuman kurang efektif. Untuk lebih
efektifnya adalah mengerti teorinya maka dengan sendiri akan kita ingat ketika
ujian. Kalau Anda masih dalam metode belajar dengan menghafal, sangat
disarankan untuk pindah ke metode mengerti materi.</span></p>

